#+   # Addition
5 + 3        # 8

#-   # Subtraction
10 - 4       # 6

#*   # Multiplication
6 * 7        # 42

#/   # Division (always returns float)
10 / 4       # 2.5

#//  # Floor division (rounds down)
10 // 4      # 2
9 // 4       # 2
-9 // 4      # -3

#%   # Modulus (remainder)
10 % 3       # 1
15 % 4       # 3

#**  # Exponentiation (power)
2 ** 3       # 8
9 ** 0.5     # 3.0

#+x  # Unary plus (keeps number positive)
x = 5
+x           # 5

#-x  # Unary minus (makes number negative)
x = 5
-x           # -5

#+=   # Add and assign
x = 5
x += 3       # 8

#-=   # Subtract and assign
x = 10
x -= 4       # 6

#*=   # Multiply and assign
x = 6
x *= 2       # 12

#/=   # Divide and assign
x = 10
x /= 4       # 2.5

#//=  # Floor divide and assign
x = 9
x //= 2      # 4

#%=   # Modulus and assign
x = 10
x %= 3       # 1

#**=  # Exponent and assign
x = 2
x **= 3      # 8

